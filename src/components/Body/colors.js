const colors = (type)=>{
    if(type==='fire'){return'#FDDFDF'}
    else if(type==='electric'){return'#FCF7DE'}
    else if(type==='grass'){return'#DEFDE0'}
    else if(type==='water'){return'#DEF3FD'}
    else if(type==='ground'){return'#f4e7da'}
    else if(type==='rock'){return'#d5d5d4'}
    else if(type==='fairy'){return'#fceaff'}
    else if(type==='poison'){return'#98d7a5'}
    else if(type==='bug'){return'#f8d5a3'}
    else if(type==='dragon'){return'#97b3e6'}
    else if(type==='psychic'){return'#eaeda1'}
    else if(type==='flying'){return'#F5F5F5'}
    else if(type==='fighting'){return'#E6E0D4'}
    else if(type==='normal'){return'#F5F5F5'}
}

export default colors